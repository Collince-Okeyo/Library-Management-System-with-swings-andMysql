    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try
        {
            String sql = "insert into students (fname,sname,regno,year,faculty,department,course,noIssued) value (?,?,?,?,?,?,?,?)";

            pst = conn.prepareStatement(sql);
            pst.setString(1,jTextField1.getText());
            pst.setString(2,jTextField2.getText());
            pst.setString(3,jTextField3.getText());
            pst.setString(4,(String)jComboBox1.getSelectedItem());    
            pst.setString(5,(String)jComboBox2.getSelectedItem());
            pst.setString(6,jTextField6.getText());
            pst.setString(7,jTextField7.getText());
            pst.setInt(8,0);
            pst.execute();
            JOptionPane.showMessageDialog(null,"Student added Successfully");
            pst.close();

        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"The student is already registered");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

