    public void search()
    {
        String usr = jTextField1.getText();
        
        String sql = "select *from admins where username = '"+usr+"' ";
        try
        {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next())
            {
            jTextField2.setText(rs.getString(1)+" "+rs.getString(2));
            jTextField3.setText(rs.getString(6));
            rs.close();
            pst.close();
            }
            else{
                JOptionPane.showMessageDialog(null,"The username does not exist in our database");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        finally{
                     try
                     {
                         rs.close();
                         pst.close();
                     }catch(Exception e)
                     {
                         
                     }
                 }
    }
    
    public void retrieve()
    {
        String a2  = jTextField4.getText();
        String usr = jTextField1.getText();
        String sql = "select *from admins where username = '"+usr+"' and answer ='"+a2+"' ";
        try
        {
        pst  = conn.prepareStatement(sql);
        rs = pst.executeQuery();
        
        if(rs.next())
        {
            jTextField5.setText(rs.getString(5));
            rs.close();
            pst.close();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Wrong answer!");
        }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        finally{
                     try
                     {
                         rs.close();
                         pst.close();
                     }catch(Exception e)
                     {
                         
                     }
                 }
        
    }

